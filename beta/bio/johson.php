			<h2>Michael O. Johnson, Chairman and CEO, Herbalife Ltd.</h2>
		 <p> He is the voice of the company to our Distributors, sets the vision and strategy, and ensures that the company has the resources necessary to meet its goals.</p>
 <p>
Michael O. Johnson has a track record of success as a leader. Named chief executive officer of Herbalife in 2003 and chairman in 2007, he sets the vision and strategy, and is the voice of the company to approximately 2.1 million Independent Distributors in 75 countries. He oversees all aspects of the company's global operations, ensures the company has the resources necessary to meet its goals and pursues profitability and growth to increase shareholder value. </p>

 <p>Under his direction, the company had five years of double-digit sales growth and has doubled  from net sales of $1.1 billion in 2003 to $2.7 billion in 2010  as well as building a global brand through its sponsorship of more than 100 teams, athletes and fitness events around the world, including the LA Galaxy, FC Barcelona, the LA Triathlon, Amgen Tour of California and American Youth Soccer Organization (AYSO). </p>
 <p>
              Michael has strengthened Herbalife's product development and manufacturing through the creation of Nutrition Advisory Board, the continued funding of the Mark Hughes Cellular and Molecular Nutrition Laboratory at UCLA, research and clinical studies at other leading universities, the development of the Herbalife Product and Science Center in Los Angeles and the Herbalife Innovation & Manufacturing facility in Lake Forest, California, and the creation of the Herbalife Nutrition Institute. 
</p>
<p>
Michael is also chairman of the Herbalife Family Foundation (HFF), which is funded through donations from Herbalife, its Independent Distributors, employees, families and friends. In 2005, HFF launched its worldwide Casa Herbalife program to help bring good nutrition to needy children. Its first US-based site was at A Place Called Home and there are now more than 60 Casa Herbalife programs on six continents. </p>
<p>
Outside the boardroom, Michael is a fitness enthusiast and has been participating in triathlons and cycling races for more than 20 years. He has encouraged Herbalife employees and Independent Distributors to join with him in participating in triathlons, marathons, cycling and fitness events around the world. Prior to Herbalife, Michael spent 17 years with The Walt Disney Company, serving as president of Disney International prior to joining Herbalife. </p>
                    

