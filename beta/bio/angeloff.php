<h2>Dann V. Angeloff, President, The Angeloff Company</h2>
		 <p> Dann Angeloff is the President of The Angeloff Company which he founded in Los Angeles in 1976.  The company is a corporate financial advisory firm advising top management of small and mid-sized companies in the areas of capital sourcing, merger-acquisition and other financial services.  He is and has been active in the capital markets as an investment banker and corporate financial advisor for over 50 years and has been responsible for over 80 financial transactions with the major emphasis in initial public offerings (IPOs).</p>
 <p>
Since the early days of his career, he has been very interested in board activity both as a director of many public and private companies as well as being a founding member of the National Association of Corporate Directors (NACD), a 11,000 membership group headquartered in Washington D.C.  He is Chairman Emeritus of the Southern California Chapter of the NACD, after serving as President and Chairman.</p>

