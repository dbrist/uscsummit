			<h2>Bernard F. Mathaisel, Executive Advisor, Achievo Corporation</h2>
		 <p> Bud Mathaisel currently serves as Executive Advisor to Achievo Corporation.  In 2007, he joined Achievo as Senior Vice President and CIO, and then was assigned to be COO North America and Europe.  Achievo is a US HQ Company providing IT and software services with over 1,700 employees worldwide, 80 percent of whom are software engineers in China.
