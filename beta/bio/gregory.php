                        <h2>Gregory E. Lau, Managing Director, RSR Partners</h2>
<p>He is a senior member of the firm's Board of Directors Practice. With more than 30 years of corporate governance experience, Greg has deep experience working closely with chairmen, chief executive officers, and nominating and governance committees on board composition and recruiting.</p>

<p>Greg previously served as executive director of global compensation and corporate governance for General Motors. In 1984, following several assignments with the GM Financial staff, Mr. Lau was named assistant treasurer of compensation, governance, benefits, and risk management in the New York Treasurer's Office. From 1982 through 2010, he was secretary of the executive compensation committee of the compan's board of directors. In 1994, he was named executive director of global compensation and corporate governance as part of GM's Global Human Resources staff and was also named secretary of the directors and corporate governance committee of GM's board</p>

<p>He is a member of the board of the National Association of Corporate Directors (NACD) and was a former member of the advisory board for the Weinberg Center for Corporate Governance. He was a member and past chairman of The Conference Board's Councils on Executive Compensation and Global Human Resources Management and a former member of the Wharton School's Executive Education advisory board. Mr. Lau also served previously on the boards of directors of WorldatWork, General International Ltd., and GMAC Insurance Holdings.</p>

<p>Greg earned his MBA from the University of Michigan and his B.A. from Dartmouth College. Greg and his wife live in Westport, CT.</p>
                    

