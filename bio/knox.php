<h2>Andrew W. Knox</h2><h3>Managing Director, RSR Partners.</h3>
<p>
Andy Knox joined RSR Partners as a Managing Director early in 2013
following nearly three years with HigdonBraddockMatthews LLC, where he
had opened and built the Los Angeles Office for this boutique executive
recruiting firm focused on financial services clients. Founded in 1993, RSR
Partners conducts senior- level executive recruiting, board recruiting,
assessment and corporate governance consulting for clients nationwide from
offices in Greenwich, New York City, Chicago, Los Angeles and Cleveland.
With more than 30 years of experience in the executive recruiting industry,
including 24 years with Korn/Ferry in the U.S. and Japan as a
partner/shareholder, Andy has executed senior-level client work in the U.S.
and internationally, including Asia/Pacific, Europe and Latin America. He has
worked across industry sectors, geographic markets and functional disciplines,
including financial services, consumer products and services, technology,
professional services, sports and entertainment. Working from a base in
Tokyo, Andy was responsible for Korn/Ferry’s activities in Japan and Korea
from 1985 through 1990. He has worked extensively throughout Asia/ Pacific
during and since that time. Prior to joining Korn/Ferry, Andy spent four years
in the Los Angeles office of Russell Reynolds Associates, Inc., another leading
executive recruiting firm. He entered the executive recruiting industry from
Bank of America in Los Angeles. Andy played a leading role in the planning
and operational management of the aquatic competitions for both the 1984
(Los Angeles) and 1988 (Seoul) Olympic Summer Games.
</p>
<p>
Andy is a member of the Executive Committee of the Asia Society/Southern
California Center and a member of the Pacific Council on International Policy.
He serves on the Board and is an officer of the Southern California Committee
for the Olympic Games, the Board of the Library Foundation of Los Angeles,
the Board of the Los Angeles Sports Council, and he served on the Board of
the USA Swimming Foundation from its founding in 2003 until the end of
2010. Andy earned an MBA from the University of Southern California and a
bachelor’s degree in history from UCLA.
</p>
