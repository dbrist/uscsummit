                        <h2>Gregory E. Lau</h2><h3>Managing Director, RSR Partners</h3></br>
<p>
                   Mr. Lau is a senior member of the firm's Board of Directors Practice.  With more than 30 years of corporate governance experience, Greg has deep experience working closely with chairmen, chief executive officers, and nominating and governance committees on board composition and recruiting.
</p>
<p>
Greg previously served as Executive Director of Global Compensation and Corporate Governance for General Motors.  In 1984, following several assignments with the GM Financial staff, he was named Assistant Treasurer of Compensation, Governance, Benefits, and Risk Management in the New York Treasurer's Office.  From 1982 through 2010, Greg was Secretary of the Executive Compensation Committee of the company's Board of Directors.  In 1994, he was named Executive Director of Global Compensation and Corporate Governance as part of GM's Global Human Resources staff and was also named Secretary of the Directors and Corporate Governance Committee of GM's Board.
Mr. Lau is a member of the board of the National Association of Corporate Directors (NACD), a former member of the advisory board for the Weinberg Center for Corporate Governance, as well as a former member and past Chairman of The Conference Board's Councils on Executive Compensation and Global Human Resources Management.  He is also a former member of the Wharton School's Executive Education advisory board.  
</p>
<p>
Additionally, he served on the boards of directors of WorldatWork, General International Ltd., and GMAC Insurance Holdings.
Greg earned his MBA from the University of Michigan and his B.A. from Dartmouth College.  Greg and his wife live in Westport, CT.
</p>
  

