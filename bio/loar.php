<h2>Stan Loar</h2>
<h3>Chairman of the board, Woodruff-Sawyer &amp; Co.</h3>
<p>
Stan plays a primary role in driving the Woodruff-Sawyer & Co. vision and
presence in the marketplace, as well as in managing our international
brokerage relationships. Stan joined Woodruff- Sawyer in 1976 and served as
the company’s Chief Executive Officer for 12 years prior to transitioning to
Vice Chair in 2008 and to Chairman in 2013.
Stan has spent 44 years in the insurance industry in a number of central roles.
In addition to having been chairman of Assurex Global and its international
executive committee, he was president of the Western Association of Insurance
Brokers (WAIB) and director of the Insurance Brokers & Agents of the West.
He was also a board member and treasurer of the National Association of
Insurance Brokers (NAIB).
</p>
<p>
Stan is currently a board member of the Council of Insurance Agents &
Brokers and an underwriting member of Lloyd’s of London. He also serves on
the board of Bermuda captives PAR Ltd. and AGP. He has extensive
international experience, including working abroad in the United Kingdom and
Germany.
</p>
<p>
Stan received his master’s degrees from the University of Southern California
(USC) and the American Graduate School of International Management
(Thunderbird). He is a Chartered Property and Casualty Underwriter (CPCU).
</p>
                    

