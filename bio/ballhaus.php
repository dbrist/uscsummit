			<h2>William F. Ballhaus</h2>
                        <h3> Jr., President and CEO (Ret.), The Aerospace Corporation</h3></br>
			<p>Dr.  William F. Ballhaus, Jr., is the former president and chief executive officer  of The Aerospace Corporation.&nbsp; He joined  Aerospace as president in September 2000 after an 11-year career with Lockheed  Martin Corporation. At Lockheed Martin Dr. Ballhaus served as corporate officer  and vice president, Engineering and Technology. Prior to his tenure with  Lockheed Martin, Dr. Ballhaus served as president of two Martin Marietta  businesses, Aero and Naval Systems and Civil Space and Communications.&nbsp; Before joining Martin Marietta, Dr. Ballhaus  served as director of NASA&rsquo;s Ames Research Center at Moffett Field and Edwards  Air Force Base, California.&nbsp; As a NASA  research scientist, Dr. Ballhaus published more than 40 papers on computational  aerodynamics. He is a graduate of the University of California at Berkeley  where he earned a Ph.D. in engineering and bachelor&rsquo;s and master&rsquo;s degrees in  mechanical engineering.&nbsp; Currently he  serves on the USC Viterbi School of Engineering Board of Councilors.</p>
<p>Dr.  Ballhaus serves on the Board of Directors of OSI Systems, a publicly traded  company.&nbsp; He also serves on the Board of  Directors of the Charles Stark Draper Laboratory.&nbsp; In February, 2012, he retired from the Board  of Directors of the Space Foundation after 10 years, the last two serving as  Chairman.&nbsp; He previously has served on  the boards of Aerospace Corporation, Sandia Corporation (Sandia National  Laboratories), Energy Research Corporation (Oak Ridge National Lab), MIT  Lincoln Laboratory and others.&nbsp; Dr.  Ballhaus is a member of the Jet Propulsion Laboratory Advisory Council and he  is also a member of the NASA Advisory Council and chairs its Technology and  Innovation Committee.	  </p>
                    

