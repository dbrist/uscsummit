			<h2> Wendy Shiba</h2>
                        <h3>2012-2013 President, National Asian Pacific American Bar Association</h3></br>
                
                <p> Ms. Shiba retired as Executive Vice President, General Counsel and Secretary of KB Home, a NYSE-listed company headquartered in Los Angeles. Previously, she was Senior VP, Chief Legal Officer and Secretary of PolyOne Corporation and VP, Secretary and Assistant General Counsel of Bowater Incorporated, both NYSE-listed global manufacturing companies. Earlier, she was Corporate Chair of the City of Philadelphia Law Department; a tenured Associate Professor of Law at Temple University School of Law; in private practice with O'Melveny & Myers; and law clerk to Justice Stanley Mosk on the Supreme Court of California. She has served as an arbitrator for the Philadelphia Stock Exchange and is co-author of the reference book, Pennsylvania Corporation Law and Practice. </p>
                <p> Wendy was President of the National Asian Pacific American Bar Association (NAPABA) and Vice Chair of the Board of Trustees for the Japanese American National Museum. She was a director of Bowater Canada Inc. (listed on the Toronto Stock Exchange), has held board and leadership positions with numerous professional and charitable organizations, and has served by gubernatorial appointment on the South Carolina Board of Accountancy.  </p>
                <p> She is a member of the 2007 inaugural class of the DirectWomen Board Institute (an initiative co-sponsored by the American Bar Association and Catalyst to identify, develop and support a select group of accomplished women lawyers to serve on U.S. corporate boards) and has been a frequent keynote speaker and panelist on topics including corporate governance, securities law, women's leadership, and diversity and inclusion in the legal profession. Wendy was recognized as one of the 2010 Most Powerful and Influential Women in California and received NAPABA's 2009 Trailblazer Award and the YWCA's 2007 Woman of Achievement Award.  </p>
                <p> Wendy earned her B.S. from Michigan State University and her J.D. from Temple University School of Law, where she graduated first in her class and was Articles Editor of the Law Review. </p>

