been honored as one of the top 100 influential people in finance by Treasury &
Risk Management and Business Finance, as well as in accounting by
Accounting Today. Colleen Cunningham earned a B.A. in Economics from
Rutgers University and an MBA from New York University's Stern School of
Business where she was a Stern Scholar in the Executive Management
