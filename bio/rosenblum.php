        <p> David Rosenblum is the President of the NACD Southern California Chapter; Vice Chair of the Board of the Library Foundation of Los Angeles; and a trustee of Wesleyan University.  A senior partner with Deloitte Consulting LLP prior to his retirement in June 2013, Mr. Rosenblum spent over 30 years advising clients in a wide range of industries, particularly healthcare and financial services, on issues related to strategy, M&A, divestitures/restructuring and financial management.  He held a number of leadership positions within Deloitte Consulting, including COO for an $800 million business unit and National Managing Director for Corporate Development, as well as serving on the board of Deloitte Consulting.  He has authored articles for publications such as the Harvard Business Review; spoken at conferences hosted by organizations such as the Conference Board and the NACD; and been a guest lecturer at numerous colleges and universities.  Mr. Rosenblum served as chair of the Echo Foundation from 2004-2009. </p>
        
                    

