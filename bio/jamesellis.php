                   <h2>James Ellis</h2><h3>Dean, USC Marshall School of Business</h3><br/>
<p>
James G. Ellis was appointed dean of the USC Marshall School of Business
and holder of the Robert R. Dockson Dean’s Chair in Business Administration
in April 2007. He also has held a full-time appointment as a professor in the
Department of Marketing since 1997, and continues to teach the school’s
freshman leadership colloquium class.
</p>
<p>
Prior to being appointed dean, Ellis was USC’s vice provost for globalization,
responsible for building the USC name worldwide. At the USC Marshall
School, he previously served as vice dean for external relations and as
associate dean for undergraduate programs. From 1998 to 2003, he directed
the Family Business Program, running seminars for families in business.
Ellis worked in the corporate world from 1970 to 1997. He was president and
CEO of Porsche Design from 1985 to 1990. From 1990 to 1997, he was
chairman and CEO of Port O’Call Pasadena, an upscale home accessory
retailer, as well as an owner/partner in six other companies.
Ellis was a member of the Young Presidents Organization for 14 years, during
which time he served two terms as chairman of the San Gabriel Valley Chapter
and one term as regional vice president, and for six years was a member of the
International Board of Directors. He is also a past chairman of the Pasadena
Chamber of Commerce.
</p>
<p>
Ellis sits on numerous corporate and nonprofit boards and has addressed
organizations around the world on business topics.
At USC in 2003, Ellis was honored with both the Teaching Has No Boundaries
Award, co-sponsored by the university’s Center for Excellence in Teaching,
and the Golden Apple Award, given by USC Marshall School students. In
2004, he received the USC Parents’ Association’s Outstanding Teaching and
Mentoring Award.
</p>
<p>
Ellis holds an M.B.A. from the Harvard Business School and a B.B.A. from
the University of New Mexico.
</p>

                    

