<p>
In 2010, Ms. Feeney launched Risk for GoodTM (R4G), a board advisory firm
that works exclusively with Independent Directors who want to strengthen risk
oversight in the boardroom. Prior to R4G, she founded Envision Strategic
Group (ESG) to provide advisory services on emerging business trends that
impact strategy and growth. This includes Risk Management, Corporate Social
Responsibility & Sustainability, and Human Capital. Her practice includes
global companies in the manufacturing, arts & entertainment, hospitality, and
golf industries. Risk for Good and Envision Strategic Group were built on the
expertise of 20 years working with Fortune 100 enterprises (CFO and Risk
Managers) in the Loss Prevention & Engineering department of Travelers
Insurance Company. Ms. Feeney has recently produced, in conjunction with
the Risk and Insurance Management Society, a panel session entitled “Risk
Intelligence” for the UCLA Anderson School Directors Education and
Certificate Program. A sought after volunteer leader, Ms. Feeney was recently
appointed Vice-Chair of the ASSE Foundation focused on fundraising with the
Fortune 100. She was also a Chief Executive Officer appointee of the Los
Angeles County Risk Management Advisory Committee. In 2007, Ms. Feeney
was named “Women of the Year” by the Manhattan Beach Chamber of
Commerce Women in Business Committee and was on the Workplace HR &
Safety Magazine “Who’s Who” in the safety industry.
</p>
<p>
In 2009, Ms. Feeney achieved the Certificate of Director Education through
NACD’s Director Professionalism®. She received her B.S. degree in Health
Sciences & Safety from San Diego State University. Ms. Feeney is a Certified
Safety Professional and has an Associate in Risk Management certificate from
the Insurance Institute of America.
