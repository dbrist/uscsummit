<p>
Recipient of Three of the American Accounting Association's Notable
Contributions to Research Awards, Ken Merchant is an expert in management
accounting, management control systems, and ethics. He has published in
many top journals, including Accounting, Organizations and Society, The
Accounting Review, Journal of Accounting Research, Journal of Accountancy,
Journal of Marketing, and Management Science. He is also the author of Blind
Spots, Biases, and other Pathologies in the Boardroom, published in 2010, and
nine other books.
</p>
<p>
He is currently serving as an editor of The Accounting Review and two book
collections and on the editorial boards of 10 other academic journals, including
Corporate Governance: An International Review. He has received four
Notable Contributions to Research awards from the American Accounting
Association, two for lifetime contributions to the management accounting and
behavioral accounting fields. He was awarded an honorary doctorate from the
Turku School of Economics (Finland) in 2010. Professor Merchant served as
Dean of the Leventhal School of Accounting from 1994 to 2001.
Before joining USC, Professor Merchant taught at Harvard Business School
and the University of California, Berkeley.
