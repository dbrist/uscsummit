<h2>Bryan Stockton</h2>
<h3>Chairman and Chief Executive Officer, Mattel, Inc.</h3>
<p>
Bryan Stockton is Chief Executive Officer of Mattel, Inc. (“Mattel” or the “Company”), the
worldwide leader in the design, manufacture and marketing of toys. He also serves as Chairman
of the Company’s Board of Directors, a role he assumed in January 2013. Mattel has annual
revenues in excess of $6.2 billion, operations in more than 40 countries, and sells products in
more than 150 countries worldwide. Stockton is building upon Mattel’s success over the past
decade to align the global organization and execute the strategic initiatives required to be the
largest, most profitable and innovative toy company in the world, driving sustainable growth and
innovation across the business to leverage the Company’s scale and global structure.
Prior to being named Mattel’s sixth Chief Executive Officer in early 2012, Stockton was
Mattel’s Chief Operating Officer. He had responsibility for all of the Company’s sales and
operations, which included Mattel Brands, Fisher-Price, American Girl and Mattel International,
as well as the Worldwide Operations and Corporate Responsibility divisions. He was President
of $3.0 billion Mattel International before 2011, having led and grown Mattel’s international
operations over the previous seven years.
<p>
</p>
Stockton joined Mattel in November 2000 as Executive Vice President of Business Planning and
Development. In that role he was responsible for identifying and developing strategic
opportunities for the Company, as well as managing all merger and acquisition activity.
Stockton assumed responsibility for the Company’s international operations in 2003 as
Executive Vice President of International, driving record financial and operational performance
across Mattel’s businesses outside the U.S., including sales and marketing for Barbie, Hot
Wheels and Fisher-Price. He was promoted to President of International in 2007. Under his
leadership, International generated consistent growth in gross sales and profitability annually,
and increased International’s total contribution to Mattel’s annual total sales from 36 percent to
current levels of almost 50 percent.
</p>
Prior to joining Mattel, Stockton served as President and Chief Executive Officer of Basic
Vegetable Products, LP (BVP), a privately-held company. BVP was the largest manufacturer of
vegetable ingredients in the world. In that role, he repositioned BVP into a global business by
accelerating volume growth and lowering manufacturing and distribution costs, and led the
successful sale to ConAgra Foods. Before joining BVP, Stockton held a variety of positions
during his 22-year tenure with Kraft Foods, Inc., the largest U.S.-based packaged food company
in the world. He last served as President of North American Foodservice.
Stockton earned a BS degree with honors in Business from Indiana University and a Masters of
Business Administration degree from the Indiana University Graduate School of Business. He
served on the Board of Bob Evans Farms, Inc. from 2006 to August 2012; held a variety of
senior leadership roles on the Toy Industry Association, including two years as Chairman; was a
member of the Board of Mattel Children’s Hospital UCLA from 2001 to April 2010; served as
Chairman of World Trade Week Southern California; and currently serves as a member of the
President’s Advisory Council at Otis College of Art & Design; UCLA Anderson School Board
of Visitors; and the Board of Directors of the 2015 Special Olympics World Summer Games Los
Angeles.
</p>
                    

