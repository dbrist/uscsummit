			<h2>Myron T. Steele</h2>
                        <h3> Chief Justice, Delaware Supreme Court</h3></br/>
            <p> The Senate confirmed Governor Ruth Ann Minner's nomination of Justice Myron T. Steele as Chief Justice on Wednesday, May 5, 2004. Chief Justice Steele is the 7th Chief Justice of the Delaware Supreme Court. Before his appointment as Chief Justice, he served as a Supreme Court Justice from July 28, 2000 to May 5, 2004. Previously, he served as a Vice Chancellor of the Court of Chancery from 1994 to 2000, as Resident Judge of the Superior Court in Kent County from 1990 to 1994, and as a Superior Court Judge from 1988 to 1990. </p>
            <p> Chief Justice Steele graduated from the University of Virginia (B.A., Foreign Affairs, 1967) and the University of Virginia School of Law (J.D., 1970; LL.M. 2005). He served on active duty in the U.S. Army and retired as a Colonel in the Delaware Army National Guard. He was a Deputy Attorney General, Senate (Delaware) Attorney and Chairman of the Consumer Affairs Board. Before being appointed to the bench, he was a litigation partner in Prickett, Jones & Elliott of Wilmington and Dover. He also served as outside counsel, Director and Chairman of the Central Delaware Health Care Corporation. </p>
            <p> In addition to his judicial activities, Chief Justice Steele has been appointed to the Judicial Conference Committee on Federal-State Jurisdiction by Chief Justice John Roberts. He is an advisor to the Business Law Section of the American Bar Association, a member of and judicial liaison to the Negotiated Acquisitions Committee of the Business Law Section, and a member of the Judicial Section. He is also a member of the American Board of Trial Attorneys, the first member of the Delaware Judiciary selected. </p>
            <p> As Vice Chancellor and Superior Court Judge, Chief Justice Steele presided over major corporate litigation and LLC and limited partner governance disputes. Some of the most noteworthy trials over which he presided include the Viacom/Universal Studios dispute over ownership of the USA Television Networks; Painewebber v. Centocor, an internal governance dispute in a nationally traded limited partnership; CFLP v. Cantor, et al., a dispute seeking injunctive and contractual remedies between limited partners and a general partner in a closed partnership; and the DuPont v. Admiral environmental insurance coverage litigation. Chief Justice Steele has published over 400 opinions resolving disputes among members of limited liability companies and limited partnerships, and between shareholders and management of both publicly traded and closely held corporations. </p>
            <p> Chief Justice Steele speaks and writes frequently on issues of corporate document interpretation and corporate governance. His thesis for the LL.M. degree, Judicial Scrutiny of Fiduciary Duties in Delaware Limited Partnerships and Limited Liability Companies, focused on the application of common law fiduciary duties within the contractual framework of alternative business organizations. It was published in the Delaware Journal of Corporate Law (32 Del. J. Corp. L. 1 (2007)). The November 2005 issue of The Business Lawyer included an article he co-authored with Sean J. Griffith entitled On Corporate Law Federalism: Threatening the Thaumatrope (61 Bus. Law. 1 (2005)). Most recently, he co-authored an article with J.W. Verret entitled Delaware's Guidance: Ensuring Equity for the Modern Witenagemot to be published in the Fall 2007 issue of the Virginia Law & Business Review (2 Va. L. & Bus. Rev. 188 (2007)). That article formed the basis for a keynote speech to the Business Section at the 2007 ABA Annual Meeting. </p>
            <p> Directorship Magazine has frequently ranked Chief Justice Steele as one of the 100 most influential people in corporate governance in the United States. In December  2007, Ethisphere Magazine ranked Chief Justice Steele second in its list of "the 100 Most Influential People in Business Ethics for 2007." Lawdragon Magazine has consistently placed Chief Justice Steele among its annual Lawdragon 500 "Leading Lawyers in America" and "Top Judges in America." </p>
            <p> Chief Justice Steele will retire November 30, 2013. </p>

