<h2>Todd Kaltman</h2>
<h3>Managing Director, Duff & Phelps</h3>
<p>
Todd Kaltman is a managing director in Duff & Phelps' Investment Banking
unit, with a specialization in transaction opinions, which includes fairness and
solvency opinions as well as valuations in the context of corporate
transactions. Todd is also a member of the firm's Senior Review Committee,
which approves all fairness and solvency opinions issued by Duff & Phelps.
He has been with Duff & Phelps for more than 13 years and has nearly 25
years of experience providing corporate and securities valuation and financial
advisory services.
</p>
<p>
Over the course of his career, Todd has provided financial opinions and advice
to public and private companies in relation to mergers and acquisitions,
recapitalization transactions, spin-offs, debt and equity securities issuance,
joint venture formation and employee stock ownership plans. Todd has also
provided valuations related to tax and financial reporting, shareholder buy/sell
agreements, and marital and partnership dissolution.He has experience across a
wide range of industries, including healthcare, financial services, food and
agribusiness, manufacturing, professional services, technology and
communications. Some of noteworthy transactions on which Todd has advised
corporate boards include: The spin-off of Marriott Vacations Worldwide Corp.
by Marriott International, Inc. (solvency opinion), The recapitalization and
spin-off of Ticketmaster, HSN, Tree.com, and Interval Leisure Group by
IAC/InterActiveCorp (multiple solvency opinions), The sale of Earle M.
Jorgensen Company to Reliance Steel & Aluminum Co (fairness opinion), The
sale by Del Monte Corporation of its seafood business, including the StarKist
brand (fairness opinion) and The reverse/forward split of common stock by
Travelzoo, Inc. (fairness opinion).
</p>
<p>
Todd received his B.S. in business administration, magna cum laude, from
California State University, Northridge. He is a chartered financial analyst
(CFA) charterholder and he holds the Financial Industry Regulatory Authority
Series 7 and 63 licenses. He is also a member of the CFA Institute, the CFA
Society of Los Angeles, the National Association of Corporate Directors and
the Association for Corporate Growth.
</p>


