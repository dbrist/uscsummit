			<h2>Duke Bristow</h2><h3>Associate Professor, USC Marshall School of Business</h3></br>
<p>
Professor Bristow is an expert on corporate finance and corporate governance,
particularly in the areas of private equity and director education. Dr. Bristow
spent 10 years in finance and marketing in the Pharmaceutical, Medical
Device, and Diagnostics Divisions and in Corporate Finance with Eli Lilly &
Company. In the 20 years since returning to academia, he has been a director
and advisor. In 1999, he founded the Director Training and Certification
Program, the first corporate governance educational program in the US using
entrance and exit examinations. His academic interests concern the financing,
governance, regulation and valuation of high growth firms. He has published
papers in economics, engineering, and law journals, and has advised on
matters involving corporate governance and director education since 1998. His
research and teaching has received support from the National Institutes of
Health, the NASDAQ, the four largest international audit firms, and a dozen
national law firms. He began teaching finance at the University of Southern
California in 2006. He won the prestigious Golden Apple Teaching Award in
2009 and the Premier Professor award in 2013, Dr. Bristow incorporates
current events from the Wall Street Journal into his lectures making what
happens in the economy and business world accessible to any dedicated
student.
</p>
<p>
Dr. Bristow has been on both sides of hostile proxy matters and provided
expert witness support on governance issues: twice he was an Oracle nominee
for the PeopleSoft board. Lions Gate Films nominated him for the Image
Entertainment Board. He was an Express Scripts nominee in the
Caremark/CVS matter and a Community Health Systems nominee for the
Tenet Board. He has chaired or served on all of the primary board committees
