<h2>Anne Sheehan</h2>
<h3>Director of Corporate Governance, California State Teachers' Retirement System (CalSTRS)</h3>
        <p> Anne Sheehan is director of corporate governance for the California State Teachers' Retirement System (CalSTRS), the largest teacher's public pension fund in the U.S. She is responsible for overseeing all corporate governance activities for the fund including proxy voting, company engagements and managing $3 billion placed with activist managers. Previously, Sheehan served as chief deputy director for policy at the California Department of Finance. During her tenure there, she served on both the CalSTRS and the California Public Employees' Retirement System (CalPERS) boards and as executive director of the state of California's Post-Employment Benefits Commission. Sheehan currently serves as chair of the Council of Institutional Investors, is a member of the Securities and Exchange Commission's Investor Advisory Committee, the Nasdaq Listing Council and the Sustainability Accounting Standards Board. She was named one of the 100 most influential people on corporate governance by Directorship magazine in 2009, 2010, 2011 and 2012.  </p>
        
                    

