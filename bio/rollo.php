			<h2>Robert S. Rollo</h2>
			<h3>Retired Partner, Heidrick and Struggles; Global Board and CEO Practice, NACD</h3></br>
            
            <p> During his 24 years in executive search, Bob Rollo was recognized as one of the nation's leading recruiters in "The Global 200," "The New Career Makers," "The Rites of Passage" and "Headhunters and How to Use Them." He was successful in completing more than 450 senior-level executive searches, including board member, CEO, president and COO positions. Among the most noteworthy were the President of USC, Steven Sample, and the Chairman of the US Olympic Committee, Peter Ueberroth.  </p>
