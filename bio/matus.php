                        <h2>Gary J. Matus</h2>
                        <h3>Managing Director, RSR Partners</h3>
<p>
Gary J. Matus joined RSR Partners in 2010 as a Managing Director and is a
senior member of the Financial Services, Board and Consumer practices. Mr.
Matus opened RSR’s California practice. Prior to this, Mr. Matus was a
Managing Partner with Egon Zehnder International. Mr. Matus initially
launched and managed the California practice and led the firm’s U.S. Financial
Services Practice Group and was a member of the Consumer Practice Group.
Previously, Mr. Matus served as President and CEO and a Member of the
Board of Directors and head of the Audit Committee of Iwerks Entertainment
(NASDAQ listed – IWRK). Iwerks designs and manufactures proprietary and
patented high-tech giant screen equipment, projection systems, simulation
units and produces films. Iwerks has operations in 28 countries. Mr. Matus
completed a profitable merger with Canadian-based SimEx in 2002 and
continues as a Director of SimEx.
</p>
<p>
Concurrent with the activities above, Mr. Matus served as a Partner with
BeneVenture Capital Group, a venture capital firm focused on internet,
entertainment and communications projects. BeneVenture’s portfolio includes
15 companies. Mr. Matus provided the CEO, CFO and CMO direction to a
number of the firms in BeneVenture's portfolio, including a web-based system
for e-content purchases, a business-to-business exchange and an internet
host/ASP and small business portal.
</p>
<p>
Prior to this, Mr. Matus was Executive Vice President and Chief Marketing
Officer for Bank of America, responsible for all corporate marketing, product
management and brand management. Before that bank assignment, Mr. Matus
was Executive Vice President and Industry Manager of Bank of America's
Entertainment and Media Industries Group. In that capacity, he managed the
bank's global entertainment and media portfolio with over $5 billion in
commitments serving nearly 200 clients, including major motion picture
studios, television production companies, gaming, publishing, television/radio
broadcasters, cable television, theatrical exhibitors, theme parks. Mr. Matus
began his career as a National Brands Product Manager with Colgate
Palmolive.
</p>
<p>
Mr. Matus received his BA in history and mathematics from California State
University, Los Angeles. He earned an MBA from Thunderbird School of
Global Management where he serves on the Board of Trustees. Mr. Matus also
completed the Director Education and Certification Program at University of
California, Los Angeles. He was appointed by the Governor to serve on the
Governor’s Council on Information Technology. Mr. Matus currently serves on
the YMCA Board and was previously a Trustee of Occidental College.
</p>
                    

