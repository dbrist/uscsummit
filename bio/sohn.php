                <h2>Dr. Sung Won Sohn</h2>
                <h3>Director of Forever 21, Smith Professor of Economics, California State University Channel Islands</h3>
                
                <p> Dr. Sohn is Smith Professor of Economics at California State University Channel Islands. He is a Commissioner at the Port of Los Angeles, the largest port in the nation. He also serves on the boards of a multinational retailer, Forever 21, Western Alliance Bancorporation, etc. </p>
                <p> Dr. Sohn was the President and Chief Executive Officer of Hanmi Financial Corporation, a commercial bank in Los Angeles, California. </p>
                <p> Before joining Hanmi in 2005, Dr. Sohn was an Executive Vice President and Chief Economic Officer of Wells Fargo Banks, the fourth largest bank in the United States. He is the author of a book, Global Financial Crisis and Exit Strategy published in 2009. Prior to his tenure at Wells Fargo, Dr. Sohn was a senior economist on the President's Council of Economic Advisors in The White House. He was responsible for economic and legislative matters pertaining to The Federal Reserve and financial markets. Dr. Sohn has also been a tenured college professor in Pennsylvania State University System. </p>
                <p> In 2007, the local Chamber of Commerce recognized him as "The Executive of the Year". In the commendation, it said, "Dr. Sung Won Sohn's outstanding management and leadership skills have set exemplary standards for the community." </p>
                <p> In 2006, The Wall Street Journal featured a story naming Dr. Sohn as the most accurate economist in the United States. In 2010, the Journal named him as one of the five best forecasters in the country. In 2012, he was ranked third. This is one of the most prestigious honor in economics profession. </p>
                <p> In 2002, he was named to the Time Magazine's Board of Economists. In 2001, Bloomberg News selected Dr. Sohn as one of the five most accurate forecasters in the United States. In 2002, Blue Chip Publications picked him as the most accurate forecaster for the Western States. </p>
                <p> He was chosen as one of the 100 most influential Minnesotans of the 20th century by the state's largest daily newspaper, The Star Tribune. Others on the list included Charles Lindberg, Sinclair Lewis, Hubert Humphrey, Walter Mondale and Charles Schultz of the Peanuts Cartoon. </p>
